public class ContainerTagsBean {
    private String SCName;
    private String SCId;
    private String SCContainer;
    private String SCClass;

    public String getSCName() {
        return SCName;
    }

    public void setSCName(String SCName) {
        this.SCName = SCName;
    }

    public String getSCId() {
        return SCId;
    }

    public void setSCId(String SCId) {
        this.SCId = SCId;
    }

    public String getSCContainer() {
        return SCContainer;
    }

    public void setSCContainer(String SCContainer) {
        this.SCContainer = SCContainer;
    }

    public String getSCClass() {
        return SCClass;
    }

    public void setSCClass(String SCClass) {
        this.SCClass = SCClass;
    }
}


