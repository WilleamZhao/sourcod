public class GridTagsBean {

    private String SCName;
    private String SCId;
    private String SCWidth;
    private String SCHeight;
    private String SCClass;
    private String SCStyle;
    private String SCParentId;

    public String getSCName() {
        return SCName;
    }

    public void setSCName(String SCName) {
        this.SCName = SCName;
    }

    public String getSCId() {
        return SCId;
    }

    public void setSCId(String SCId) {
        this.SCId = SCId;
    }

    public String getSCWidth() {
        return SCWidth;
    }

    public void setSCWidth(String SCWidth) {
        this.SCWidth = SCWidth;
    }

    public String getSCHeight() {
        return SCHeight;
    }

    public void setSCHeight(String SCHeight) {
        this.SCHeight = SCHeight;
    }

    public String getSCClass() {
        return SCClass;
    }

    public void setSCClass(String SCClass) {
        this.SCClass = SCClass;
    }

    public String getSCStyle() {
        return SCStyle;
    }

    public void setSCStyle(String SCStyle) {
        this.SCStyle = SCStyle;
    }

    public String getSCParentId() {
        return SCParentId;
    }

    public void setSCParentId(String SCParentId) {
        this.SCParentId = SCParentId;
    }
}
